def build_summary_prompt(
    score: float,
    level: str,
    user_requirement: str,
    advice_summary: str = "",
) -> str:
    """
    构建最终变化总结的提示词。

    Args:
        score: 原始评分
        level: 原始等级
        user_requirement: 用户需求
        advice_summary: 分析建议摘要

    Returns:
        完整提示词字符串
    """
    return f"""你是一位专业的医学美容顾问。请根据以下信息，总结本次美容分析的结果。

## 原始评分
- 评分：{score:.2f} / 5.00（等级：{level}）

## 用户需求
{user_requirement}

## 分析建议摘要
{advice_summary}

## 请按以下格式输出总结报告

### 1. 面部特征总结
（总结当前面部特征，说明评分依据）

### 2. 改善方向
（概括主要的可改善方面和建议）

### 3. 是否符合用户需求
（评估分析结果是否满足用户的美容需求）

### 4. 后续建议
（给出进一步的医学美容建议和注意事项）

### 5. 温馨提示
（提醒用户效果图仅供参考，实际效果因人而异，建议咨询专业医生）

## 重要要求
- 措辞温和、专业
- 不要过度承诺效果
- 强调个体差异
- 所有建议仅供参考，不构成医疗建议
- 使用中文输出"""
